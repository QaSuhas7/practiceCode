package javapractice;

class TableMulti{
	public static void main(String [] args){
	
		int first=1;
		int last=10;
		int num=0;
		
		for(int i=first;i<=last;i++){
			
			num=2*i;
			
			//System.out.println("Multiplication table of 2:");
			System.out.println("2"+"X"+ i+"="+num);	
		}
		
		
		
		
	}
}